        <section id="sejarah">
            <h2>Sejarah Gereja</h2>

            <div class="sejarah-container">

                <div class="sejarah-item">
                    <h3>Pendirian Kapel</h3>
                    <p>Sejarah Paroki Maria Ratu Pencinta Damai berawal dari keberadaan sebuah kapel di Gang Usaha 2 yang pada awalnya melayani umat Katolik yang masih termasuk dalam wilayah Paroki Katedral Santo Yosep, Pontianak.
                    Pada masa itu, gereja Katolik yang relatif mudah dijangkau oleh umat di wilayah ini hanyalah Gereja Katedral Pontianak, tetapi keterbatasan sarana transportasi membuat umat mengalami kesulitan untuk mengikuti perayaan Ekaristi secara rutin.<br><br>
                    Keinginan umat untuk memiliki tempat ibadah yang lebih dekat mulai terwujud setelah Keuskupan Agung Pontianak membeli sebidang tanah di Gang Usaha 2 pada tanggal 18 April 1966. Di atas lahan tersebut kemudian didirikan sebuah bangunan dengan izin sebagai gedung serbaguna yang difungsikan sebagai kapel.
                    Misa pertama di Kapel Gang Usaha 2 dirayakan pada Hari Raya Paskah 14 April 1968 dan menjadi tonggak awal kehidupan menggereja umat di wilayah ini. Perayaan Ekaristi perdana tersebut disambut dengan sukacita oleh umat karena untuk pertama kalinya mereka dapat mengikuti misa di lingkungan yang dekat dengan tempat tinggal mereka. 
                    </p>
                </div>

                <div class="sejarah-item">
                    <h3>Perkembangan Stasi</h3>
                   <p>Sejak tahun 1968 hingga 1974, pelayanan pastoral di Kapel Gang Usaha 2 dilakukan oleh para imam Ordo Saudara Dina Kapusin dari Paroki Katedral Pontianak yang datang secara bergiliran.
                    Pada tahun 1972, Pastor Hieronymus Herculanus Bumbun, O.F.M. Cap. diangkat sebagai Vikaris Jenderal Keuskupan Agung Pontianak dan mulai secara teratur mempersembahkan misa di Kapel Gang Usaha 2 bersama Pastor Martinus, OFM. Cap. Pada tanggal 22 Juli 1975, Pastor Bumbun ditunjuk oleh Takhta Suci sebagai Uskup Agung Pontianak menggantikan Mgr.
                    Herculanus Joannes Maria van der Burgt, O.F.M. Cap. yang menderita sakit.<br><br>
                    Sebagai uskup yang mengenal dengan baik perkembangan umat di Gang Usaha 2, Mgr. Bumbun kemudian melakukan penataan wilayah Paroki Katedral dengan menetapkan Kapel Gang Usaha 2 sebagai stasi yang menaungi sekitar 15 kring. Seiring bertambahnya jumlah umat, aktivitas pastoral dan peribadatan di Stasi Gang Usaha 2 semakin meningkat dan kapel yang ada mulai dirasakan tidak lagi memadai. 
                    </p>
                </div>

                <div class="sejarah-item">
                    <h3>Pembangunan Gedung Serbaguna</h3>
                     <p>Untuk menjawab kebutuhan akan tempat ibadah yang lebih memadai, Pastor Martinus, OFM Cap kemudian diangkat sebagai pastor paroki dan mulai mencetuskan gagasan pembangunan gereja yang baru. Pada tahun 1978, Pastor Martinus membeli sebidang tanah di wilayah Pancasila V sebagai lokasi pembangunan gereja baru.
                    Pada bulan Maret 1980, panitia pembangunan gereja resmi dibentuk dan mulai bekerja dengan langkah awal menyusun gambar bangunan gedung serbaguna sesuai gagasan Pastor Martinus. Gambar bangunan tersebut dikerjakan oleh Bruder Leopold dari Sekolah Pertukangan yang kemudian juga bertindak sebagai kontraktor pembangunan.<br><br>
                    Dalam rapat bersama Uskup Agung Pontianak pada tanggal 5 Juli 1981, panitia diminta untuk segera mengajukan permohonan Izin Mendirikan Bangunan (IMB) kepada Pemerintah Kota Pontianak.
                    Karena kendala perizinan, izin yang akhirnya diberikan oleh Wali Kota Pontianak pada tanggal 23 Oktober 1987 adalah izin sebagai gedung pertemuan umum dengan berbagai persyaratan, termasuk larangan penggunaan sebagai tempat peribadatan umum dan larangan penggunaan simbol-simbol keagamaan.<br><br>
                    Pembangunan gedung serbaguna tersebut selesai pada tahun 1988 setelah melalui masa pembangunan selama kurang lebih tiga tahun. Misa pertama di gedung baru ini dilaksanakan pada malam Natal tanggal 24 Desember 1988 dan dipimpin oleh R.D. Franciscus Yansen.
                    Meskipun secara administratif gedung tersebut belum berstatus gereja, umat tetap melaksanakan perayaan Ekaristi secara rutin di tempat tersebut. 
                    </p>
                </div>

                <div class="sejarah-item">
                    <h3>Pembangunan Gereja</h3>
                    <p>Setelah melihat perkembangan umat yang semakin pesat, pada tanggal 4 Mei 1986 status Stasi Gang Usaha 2 resmi ditingkatkan menjadi paroki dengan nama pelindung Maria Ratu Pencinta Damai. Kegelisahan umat terkait status hukum bangunan baru akhirnya terjawab setelah Keuskupan Agung Pontianak kembali mengajukan permohonan perubahan status bangunan menjadi gereja.<br><br>
                    Izin Mendirikan Bangunan sebagai gereja akhirnya diterbitkan pada tanggal 10 Mei 2010 dan ditandatangani oleh Wali Kota Pontianak Sutarmidji. Pembangunan gedung gereja Maria Ratu Pencinta Damai yang baru dimulai dengan peletakan batu pertama oleh Gubernur Cornelis pada tanggal 24 Mei 2015, bertepatan dengan Hari Raya Pentakosta.
                    Adapun bertindak sebagai Ketua Umum Panitia Pembangunan Gereja MRPD ialah Karolin Margret Natasa, anak dari Gubernur Cornelis, sementara arsitek pembangunan gereja ialah Julius Judhi.<br><br>
                    Gereja Katolik Paroki Maria Ratu Pencinta Damai Pontianak yang baru akhirnya diresmikan pada tanggal 1 Januari 2018. Adapun 1 Januari merupakan Hari Perdamaian Sedunia, dan juga Hari Raya Santa Perawan Maria Bunda Allah.
                    Gedung gereja diresmikan oleh Gubernur Kalimantan Barat Cornelis bersama dengan Uskup Agung Pontianak Agustinus Agus.<br><br>
                    Adapun pemberkatan Gereja MRPD Pancasila dilaksanakan pada tanggal 11 Maret 2018. Pemberkatan gedung gereja dipimpin oleh Nuncio Apostolik untuk Indonesia Piero Pioppo.
                    Dalam suasana Masa Prapaskah, Uskup Agung Pioppo—saat memberikan homili—mengajak umat untuk melangkah menuju Paskah bersama Kristus, Putra Allah dan Putra Maria, yang mendamaikan manusia dengan Allah Bapa serta memulihkan relasi antarsesama dalam kerukunan, saling menghormati, dan damai.
                    Misa tersebut juga dihadiri oleh Uskup Agung Pontianak Agustinus Agus dan Uskup Sanggau Giulio Mencuccini, C.P., serta Uskup Agung Emeritus Pontianak Hieronymus Herculanus Bumbun, O.F.M. Cap., bersama sekitar tiga puluh imam. Setelah komuni, Uskup Agung Agustinus Agus memberkati tabernakel serta Kapel Adorasi Ekaristi Abadi yang berada di kompleks gereja.
                    </p>
